package Loops;

public class MAleFemaleUsingSwitch {

	public static void main(String[] args) {
		String a = "male";
		switch (a) {
		case "male":
			System.out.println("male");
			break;
		case "female":
			System.out.println("female");
			break;
		default:
			System.out.println("none");
		}

	}

}
